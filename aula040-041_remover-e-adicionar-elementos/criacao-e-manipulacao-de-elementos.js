/*
Métodos para criação e manipulação de elementos DOM 

*document.createElement("tag")* -> recebe uma string com o tipo do elemento HTML que será gerado.

*element.setAttribute("atributo", valor )* -> define um valor de atributo para o elemento atual. Recebe como parâmetro uma string do atributo e o valor.

*element.getAttribute("atributo")* -> recebe uma string correspondente a um atributo do elemento atual e obtém seu respectivo valor.

*element.appendChild( elemento )* -> recebe um nó ou elemento e o define como filho do elemento atual.

*element.removeChild( elemento )* -> recebe um nó ou elemento filho do elemento atual e o remove. Se o elemento não for filho, será lançada uma exceção.

*element.contains()*-> verifica se um elemento é filho do elemento atual, retornando true ou false
*/
